export function buildUserPrompt(question: string, retrieved: { title: string; url: string; content: string }[]) {
  const sourcesBlock = retrieved
    .map((s, i) => {
      const n = i + 1;
      return `[${n}] ${s.title}\n${s.url}\n摘录：${s.content}`;
    })
    .join("\n\n---\n\n");

  return `用户问题：${question}

下面是资料库片段（你只能基于这些片段回答；请引用其中的关键点来支撑结论；如果不够就说明不确定，并列出还需要什么信息）：

${sourcesBlock || "（没有检索到资料片段）"}
`;
}

